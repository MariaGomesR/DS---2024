inf = int(input("Digite um número "))

if  (inf == 1):
    print("Domingo")

elif (inf == 2):
    print("Segunda - feira")

elif (inf == 3):
    print("Terceira - Feira")

elif (inf == 4):
    print("Quarta - feira")

elif (inf == 5):
    print("Quinta - feira")

elif (inf == 6):
    print("Sexta - feira")

elif (inf == 7):
    print("Sábado")

else:
    print("Escolha um dia da semana")